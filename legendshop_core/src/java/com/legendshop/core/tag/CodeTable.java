public class CodeTable implements Serializable {

	/** The id. */
	private String id;

	/** The name. */
	private String name;

	/**
	 * Instantiates a new code table.
	 */
	public CodeTable() {

	}

	/**
	 * Instantiates a new code table.
	 * 
	 * @param id
	 *            the id
	 */
	public CodeTable(String id) {
		this.id = id;
	}

	/**
	 * Gets the id.
	 * 
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * Sets the id.
	 * 
	 * @param id
	 *            the new id
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * Gets the name.
	 * 
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * Sets the name.
	 * 
	 * @param name
	 *            the new name
	 */
	public void setName(String name) {
		this.name = name;
	}
}
